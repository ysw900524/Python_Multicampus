# 비교연산자와 논리연산자

x = 4
y = 9

print("x == y = ", x==y)
print("x != y = ", x!=y)

print("x > y = ", x>y)
print("x < y = ", x<y)

print("int(True) = ", int(True))
print("int(False) = ", int(False))


    # x == y =  False
    # x != y =  True
    # x > y =  False
    # x < y =  True
    # int(True) =  1
    # int(False) =  0