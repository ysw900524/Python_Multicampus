# 숫자형 (Numeric type)

# 기본 연산
x = 50
y = 4

print("x = ", x)
print("y = ", y)
print("x + y = ", x+y)
print("x - y = ", x-y)
print("x * y = ", x*y)
print("x / y = ", x/y)

    # x =  50
    # y =  4
    # x + y =  54
    # x - y =  46
    # x * y =  200
    # x / y =  12.5


print("x // y = ", x//y)
print("x % y = ", x%y)
print("-x = ", -x)
print("+x = ", +x)
print("x ** y = ", x**y)
print("pow(x,y) = ", pow(x,y))

    # x // y =  12
    # x % y =  2
    # -x =  -50
    # +x =  50
    # x ** y =  6250000
    # pow(x,y) =  6250000

